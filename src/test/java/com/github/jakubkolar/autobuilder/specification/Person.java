package com.github.jakubkolar.autobuilder.specification;

import java.time.Instant;
import java.util.List;

/*
 * What a typical 'entity' or 'bean' or 'dto' or how you want to call it may look like...
 * and a typical testing headache looks like this: let's start our test by creating a few
 * Users / or let's verify that a collection contains exactly (but in any order) users of
 * given names and addresses
 */

public class Person {

    private String firstName;
    private String lastName;
    private List<String> middleNames;
    private int age;
    private Address address;
    private String login;
    private String passwordHash;
    private String email;
    private boolean emailVerified;

    private Instant created;
    private Long id;
    private Boolean deletionFlag;
    private String createdBy;
    private Instant lastModified;
    private String lastModifiedBy;

    public Person() {
        // No-arg constructor required by many frameworks
    }

    // Getters / setters again required by frameworks

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public List<String> getMiddleNames() {
        return middleNames;
    }

    public void setMiddleNames(List<String> middleNames) {
        this.middleNames = middleNames;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPasswordHash() {
        return passwordHash;
    }

    public void setPasswordHash(String passwordHash) {
        this.passwordHash = passwordHash;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public boolean isEmailVerified() {
        return emailVerified;
    }

    public void setEmailVerified(boolean emailVerified) {
        this.emailVerified = emailVerified;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public Instant getCreated() {
        return created;
    }

    public void setCreated(Instant created) {
        this.created = created;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Boolean getDeletionFlag() {
        return deletionFlag;
    }

    public void setDeletionFlag(Boolean deletionFlag) {
        this.deletionFlag = deletionFlag;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public Instant getLastModified() {
        return lastModified;
    }

    public void setLastModified(Instant lastModified) {
        this.lastModified = lastModified;
    }

    public String getLastModifiedBy() {
        return lastModifiedBy;
    }

    public void setLastModifiedBy(String lastModifiedBy) {
        this.lastModifiedBy = lastModifiedBy;
    }
}
